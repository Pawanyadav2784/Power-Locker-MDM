const CAN_SEND_TO = {
  super_admin:       ['super_distributor', 'distributor', 'sub_distributor', 'retailer'],
  super_distributor: ['distributor'],
  distributor:       ['sub_distributor', 'retailer'],
  sub_distributor:   ['retailer'],
  retailer:          [],
};

// ══════════════════════════════════════════════════════════
//  Helper: wallet balance update
// ══════════════════════════════════════════════════════════

// Universal key — koi type nahi, sabka ek hi balance
// 'running_key' = universal key balance field
const FIELD_MAP = {
  android:     'runningKeyBalance',   // alias — same field
  running_key: 'runningKeyBalance',   // primary
  iphone:      'runningKeyBalance',   // alias — same field
};

const getField = (keyType = 'running_key') => {
  const f = FIELD_MAP[keyType] || 'runningKeyBalance';
  return f;
};


const updateBalance = async (userId, keyType, amount, type) => {
  const field = getField(keyType);
  const user  = await User.findById(userId);
  if (!user) throw new Error('User not found');

  const before = user[field] || 0;
  if (type === 'debit' && before < amount)
    throw new Error(`Insufficient balance. Available: ${before}, Required: ${amount}`);

  user[field] = type === 'debit' ? before - amount : before + amount;
  await user.save();
  return { before, after: user[field] };
};

