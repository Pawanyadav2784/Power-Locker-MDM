// @desc   Dashboard stats (admin)
// @route  GET /api/keys/ledger/summary
const getLedgerSummary = async (req, res) => {
  try {
    const { from, to } = req.query;
    const matchDate    = {};
    if (from || to) {
      matchDate.createdAt = {};
      if (from) matchDate.createdAt.$gte = new Date(from);
      if (to)   matchDate.createdAt.$lte = new Date(new Date(to).setHours(23, 59, 59, 999));
    }

    const [byType, byKeyType, daily, topUsers] = await Promise.all([
      WalletTransaction.aggregate([
        { $match: matchDate },
        { $group: { _id: '$type', totalAmount: { $sum: '$amount' }, count: { $sum: 1 } } },
      ]),
      WalletTransaction.aggregate([
        { $match: matchDate },
        { $group: { _id: '$keyType', totalAmount: { $sum: '$amount' }, count: { $sum: 1 } } },
      ]),
      WalletTransaction.aggregate([
        { $match: { createdAt: { $gte: new Date(Date.now() - 30 * 24 * 60 * 60 * 1000) } } },
        { $group: { _id: { $dateToString: { format: '%Y-%m-%d', date: '$createdAt' } }, total: { $sum: '$amount' }, count: { $sum: 1 } } },
        { $sort: { _id: 1 } },
      ]),
      WalletTransaction.aggregate([
        { $match: { type: { $in: ['credit', 'credit_foc'] } } },
        { $group: { _id: '$userId', totalReceived: { $sum: '$amount' } } },
        { $sort: { totalReceived: -1 } },
        { $limit: 10 },
        { $lookup: { from: 'users', localField: '_id', foreignField: '_id', as: 'user' } },
        { $unwind: '$user' },
        { $project: { totalReceived: 1, 'user.name': 1, 'user.email': 1, 'user.role': 1 } },
      ]),
    ]);

    res.json({ success: true, byType, byKeyType, daily, topUsers });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
};

