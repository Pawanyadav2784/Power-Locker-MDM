// ══════════════════════════════════════════════════════════
//  🔄 HIERARCHY-BASED KEY TRANSFER
//  Sender ka balance DEBIT → Receiver ka balance CREDIT
//  Sirf apne direct downline ko transfer ho sakti hai
// ══════════════════════════════════════════════════════════

// @desc   Keys transfer karo apne neeche wale ko
// @route  POST /api/keys/transfer
// @access All roles (hierarchy checked)
// @body   { toUserId, keyType, amount, description }
const transferKeys = async (req, res) => {
  try {
    const { toUserId, keyType, amount, description } = req.body;

    if (!toUserId || !keyType || !amount)
      return res.status(400).json({ success: false, message: 'toUserId, keyType, amount required' });

    if (String(toUserId) === String(req.user._id))
      return res.status(400).json({ success: false, message: 'Apne aap ko transfer nahi kar sakte' });

    const sender   = req.user;
    const receiver = await User.findById(toUserId);

    if (!receiver)
      return res.status(404).json({ success: false, message: 'Receiver user not found' });

    if (!receiver.isActive)
      return res.status(403).json({ success: false, message: 'Receiver ka account inactive hai' });

    // ── Hierarchy check: kya sender is receiver ko bhej sakta hai? ──
    const allowedRoles = CAN_SEND_TO[sender.role] || [];
    if (!allowedRoles.includes(receiver.role))
      return res.status(403).json({
        success: false,
        message: `${sender.role.replace(/_/g, ' ')} → ${receiver.role.replace(/_/g, ' ')} transfer allowed nahi hai`,
        allowedTo: allowedRoles,
      });

    // ── Parent check: sirf apna direct child ──
    // Super admin ke liye ye check skip hota hai
    if (sender.role !== 'super_admin') {
      if (String(receiver.parentId) !== String(sender._id))
        return res.status(403).json({
          success: false,
          message: 'Sirf apne direct downline ko hi transfer kar sakte ho',
        });
    }

    const qty = Number(amount);

    // ── Sender balance check ──
    const field         = getField(keyType);
    const senderBalance = sender[field] || 0;
    if (senderBalance < qty)
      return res.status(400).json({
        success: false,
        message: `Insufficient balance. Aapke paas: ${senderBalance}, Chahiye: ${qty}`,
      });

    // ── Atomic transfer: sender debit → receiver credit ──
    const senderResult   = await updateBalance(sender._id,  keyType, qty, 'debit');
    const receiverResult = await updateBalance(receiver._id, keyType, qty, 'credit');

    const note = `Transfer: ${sender.name} (${sender.role}) → ${receiver.name} (${receiver.role})`;

    // Sender ledger — DEBIT entry
    await WalletTransaction.create({
      userId:        sender._id,
      type:          'debit',
      keyType,
      amount:        qty,
      balanceBefore: senderResult.before,
      balanceAfter:  senderResult.after,
      description:   description || `Transfer to ${receiver.name}`,
      toUserId:      receiver._id,
      createdBy:     sender._id,
      note,
    });

    // Receiver ledger — CREDIT entry
    await WalletTransaction.create({
      userId:        receiver._id,
      type:          'credit',
      keyType,
      amount:        qty,
      balanceBefore: receiverResult.before,
      balanceAfter:  receiverResult.after,
      description:   description || `Received from ${sender.name}`,
      fromUserId:    sender._id,
      createdBy:     sender._id,
      note,
    });

    res.json({
      success:  true,
      message:  `✅ ${qty} ${keyType} keys transferred to ${receiver.name}`,
      sender: {
        name:       sender.name,
        role:       sender.role,
        newBalance: senderResult.after,
      },
      receiver: {
        name:       receiver.name,
        role:       receiver.role,
        newBalance: receiverResult.after,
      },
    });
  } catch (err) {
    res.status(400).json({ success: false, message: err.message });
  }
};

// ══════════════════════════════════════════════════════════
//  🔑 ADMIN TRANSFER — From any user to their direct child
//  Admin panel se hierarchy-based debit/credit
// ══════════════════════════════════════════════════════════

// @desc   Admin: fromUser ka balance deduct → toUser ko credit
// @route  POST /api/keys/admin-transfer
// @access super_admin only
// @body   { fromUserId, toUserId, amount, description? }
const adminTransfer = async (req, res) => {
  try {
    const { fromUserId, toUserId, amount, description } = req.body;

    if (!fromUserId || !toUserId || !amount)
      return res.status(400).json({ success: false, message: 'fromUserId, toUserId, amount required' });

    if (String(fromUserId) === String(toUserId))
      return res.status(400).json({ success: false, message: 'fromUser aur toUser alag hone chahiye' });

    const sender   = await User.findById(fromUserId);
    const receiver = await User.findById(toUserId);

    if (!sender)   return res.status(404).json({ success: false, message: 'Sender not found' });
    if (!receiver) return res.status(404).json({ success: false, message: 'Receiver not found' });

    // ── Hierarchy check ──────────────────────────────────────
    // super_admin can transfer between ANY two users (Return Key, Credit, etc.)
    // Non-admin: sender → direct child OR child → sender only
    const requesterIsSuperAdmin = req.user.role === 'super_admin';

    if (!requesterIsSuperAdmin) {
      const senderIsParentOfReceiver = String(receiver.parentId) === String(sender._id);
      const receiverIsParentOfSender = String(sender.parentId)   === String(receiver._id);
      const isAncestor = async (ancestorId, descendantId) => {
        let current = await User.findById(descendantId).select('parentId');
        const visited = new Set();
        while (current?.parentId) {
          const pid = String(current.parentId);
          if (pid === String(ancestorId)) return true;
          if (visited.has(pid)) break;
          visited.add(pid);
          current = await User.findById(pid).select('parentId');
        }
        return false;
      };
      const forward = senderIsParentOfReceiver || await isAncestor(sender._id, receiver._id);
      const reverse = receiverIsParentOfSender || await isAncestor(receiver._id, sender._id);
      if (!forward && !reverse) {
        return res.status(403).json({
          success: false,
          message: `${sender.name} aur ${receiver.name} ke beech koi hierarchy relation nahi hai`,
        });
      }
    }
    // super_admin: no hierarchy restriction — can take from anyone


    const qty     = Number(amount);
    const keyType = 'running_key'; // universal

    // ── Balance check ──
    const field         = getField(keyType);
    const senderBalance = sender[field] || 0;
    if (senderBalance < qty)
      return res.status(400).json({
        success: false,
        message: `Insufficient balance. ${sender.name} ke paas: ${senderBalance}, Chahiye: ${qty}`,
      });

    // ── Transfer ──
    const senderResult   = await updateBalance(sender._id,   keyType, qty, 'debit');
    const receiverResult = await updateBalance(receiver._id, keyType, qty, 'credit');

    const note = `Admin Transfer: ${sender.name} (${sender.role}) → ${receiver.name} (${receiver.role})`;

    await WalletTransaction.create({
      userId: sender._id, type: 'debit', keyType, amount: qty,
      balanceBefore: senderResult.before, balanceAfter: senderResult.after,
      description: description || `Transfer to ${receiver.name}`,
      toUserId: receiver._id, createdBy: req.user._id, note,
    });

    await WalletTransaction.create({
      userId: receiver._id, type: 'credit', keyType, amount: qty,
      balanceBefore: receiverResult.before, balanceAfter: receiverResult.after,
      description: description || `Received from ${sender.name}`,
      fromUserId: sender._id, createdBy: req.user._id, note,
    });

    res.json({
      success: true,
      message: `✅ ${qty} keys: ${sender.name} → ${receiver.name}`,
      sender:   { name: sender.name,   role: sender.role,   newBalance: senderResult.after },
      receiver: { name: receiver.name, role: receiver.role, newBalance: receiverResult.after },
    });
  } catch (err) {
    res.status(400).json({ success: false, message: err.message });
  }
};

