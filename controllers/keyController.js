// @desc   Request approve/reject (admin)
// @route  POST /api/keys/request/approve
const approveKeyRequest = async (req, res) => {
  try {
    const { transactionId, action } = req.body;
    if (!['approve', 'reject'].includes(action))
      return res.status(400).json({ success: false, message: 'action: approve | reject' });

    const tx = await WalletTransaction.findById(transactionId);
    if (!tx || tx.type !== 'request')
      return res.status(404).json({ success: false, message: 'Request not found' });

    if (action === 'approve') {
      const { before, after } = await updateBalance(tx.userId, tx.keyType, tx.amount, 'credit');
      await WalletTransaction.create({
        userId: tx.userId, type: 'credit', keyType: tx.keyType,
        amount: tx.amount, balanceBefore: before, balanceAfter: after,
        description: 'Request approved', createdBy: req.user._id,
        note: 'admin_approve',
      });
      tx.status = 'approved';
    } else {
      tx.status = 'rejected';
    }
    await tx.save();
    res.json({ success: true, message: `Request ${action}d` });
  } catch (err) {
    res.status(400).json({ success: false, message: err.message });
  }
};

