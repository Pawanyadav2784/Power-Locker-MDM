// @desc   ALL descendants (BFS: children + grandchildren + great-grandchildren)
// @route  GET /api/keys/descendants/:userId
// @access super_admin only
const getDescendantsByUser = async (req, res) => {
  try {
    const { userId } = req.params;
    const descendants = [];
    const queue       = [userId];
    const visited     = new Set([String(userId)]);

    while (queue.length > 0) {
      const currentId = queue.shift();
      const children  = await User.find({
        parentId:  currentId,
        isDeleted: { $ne: true },
        isActive:  true,
      }).select('_id name role runningKeyBalance email phone parentId');

      for (const child of children) {
        const id = String(child._id);
        if (!visited.has(id)) {
          visited.add(id);
          descendants.push(child);
          queue.push(id);
        }
      }
    }

    res.json({
      success: true,
      data: descendants.map(c => ({
        id:      c._id,
        _id:     c._id,
        name:    c.name,
        role:    c.role,
        balance: c.runningKeyBalance || 0,
        email:   c.email,
        phone:   c.phone,
      })),
    });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
};




