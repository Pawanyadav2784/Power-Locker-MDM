// @desc   Admin: multiple users ko ek saath credit
// @route  POST /api/keys/bulk-credit
// @access super_admin only
// @body   { users: [{ userId, keyType, amount }], description }
const bulkCredit = async (req, res) => {
  try {
    const { users, description } = req.body;
    if (!Array.isArray(users) || users.length === 0)
      return res.status(400).json({ success: false, message: 'users array required' });

    const results = [];
    for (const { userId, keyType, amount } of users) {
      try {
        const { before, after } = await updateBalance(userId, keyType, Number(amount), 'credit');
        await WalletTransaction.create({
          userId, type: 'credit', keyType, amount: Number(amount),
          balanceBefore: before, balanceAfter: after,
          description: description || 'Bulk credit by admin',
          createdBy: req.user._id,
          note: 'admin_generate',
        });
        results.push({ userId, success: true, balance: after });
      } catch (err) {
        results.push({ userId, success: false, error: err.message });
      }
    }

    const successCount = results.filter(r => r.success).length;
    res.json({ success: true, message: `${successCount}/${users.length} users credited`, results });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
};

